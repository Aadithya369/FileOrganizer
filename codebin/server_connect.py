import socket
server_socket = socket.socket(socket.AF_INET,socket.SOCK_STREAM)
server_socket.bind(('127.0.0.1',8080))
server_socket.listen(3)
conn1,addr1 = server_socket.accept()
conn2,addr2 = server_socket.accept()  
conn3,addr3 = server_socket.accept()  
conn1.sendall(("Hello I am from 127.0.0.1:8080").encode())
conn2.sendall(("Hello I am from 127.0.0.1:8080").encode()) 
conn3.sendall(("Hello I am from 127.0.0.1:8080").encode()) 
recevied1 = conn1.recv(1024).decode()
recevied2 = conn2.recv(1024).decode()
recevied3 = conn3.recv(1024).decode()
print(recevied1)
print(recevied2)
print(recevied3)
server_socket.close()
